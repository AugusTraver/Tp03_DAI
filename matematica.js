const PI = 3.14;
function sumar(x, y) {
    let num = x + y;
    return num;
}
const multiplicar = (a, b) => {
    let num = a * b;
    return num;
};
const dividir = (z, n) => {
    let num = z / n;
    return num;
};
const restar = (u, i) => {
    let num = u - i;
    return num;
};
export { PI, sumar, multiplicar, dividir, restar };